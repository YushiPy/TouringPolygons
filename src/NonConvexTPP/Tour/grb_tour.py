import gurobipy as gp
from gurobipy import GRB
import numpy as np

def vertices_to_halfplanes(verts: list[tuple[float, float]]) -> tuple[np.ndarray, np.ndarray]:
	n = len(verts)
	A = np.zeros((n, 2))
	b = np.zeros(n)
	for i in range(n):
		x0, y0 = verts[i]
		x1, y1 = verts[(i + 1) % n]
		dx, dy = x1 - x0, y1 - y0
		A[i] = [dy, -dx]
		b[i] = dy * x0 - dx * y0
	return A, b

def shortest_tour(polygons: list[list[tuple[float, float]]]) -> list[np.ndarray]:
	k = len(polygons)
	halfplanes = [vertices_to_halfplanes(poly) for poly in polygons]

	m = gp.Model()
	m.Params.OutputFlag = 0

	p = [m.addMVar(2, lb=-GRB.INFINITY, name=f"p_{i}") for i in range(k)]
	t = [m.addVar(lb=0, name=f"t_{i}") for i in range(k)]

	for i, (A, b) in enumerate(halfplanes):
		m.addConstr(A @ p[i] <= b)

	for i in range(k):
		d = m.addMVar(2, lb=-GRB.INFINITY, name=f"d_{i}")
		m.addConstr(d == p[(i + 1) % k] - p[i])
		m.addGenConstrNorm(t[i], d, which=2)

	m.setObjective(gp.quicksum(t), GRB.MINIMIZE)
	m.optimize()

	if m.Status != GRB.OPTIMAL:
		raise RuntimeError(f"Model status: {m.Status}")

	return [tuple(map(float, p[i].X)) for i in range(k)] # type: ignore

