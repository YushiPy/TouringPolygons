
"""
Hertel-Mehlhorn convex decomposition algorithm.

Pipeline:
  1. Ear-clip triangulation  -> list of triangles
  2. Build adjacency graph   -> triangles sharing a diagonal
  3. Greedy diagonal removal -> merge if result stays convex
  4. Return convex polygons

All polygons are represented as lists of (x, y) tuples in CCW order.
"""

