def path_length(path: Sequence[tuple[float, float]]) -> float:
	return sum(math.dist(path[i + 1], path[i]) for i in range(len(path) - 1))

def tpp_astar(start: tuple[float, float], target: tuple[float, float], polygons: Sequence[Sequence[tuple[float, float]]], *, simplify: bool = False) -> list[tuple[float, float]]:

	if len(polygons) == 0:
		return [start, target]

	polys = [[start]] + list(polygons) + [[target]]
	best = [[math.inf] * len(poly) for poly in polys]

	def heuristic(i: int, j: int) -> float:
		return math.dist(polys[i][j], target)

	# (cost, i, j) means the j-th vertex of the i-th polygon needs to be explored.
	# It has a heuristic cost of "cost", which is the length of the shortest path 
	# from the current vertex to the target.
	previous: list[list[tuple[int, int] | None]] = [[None] * len(poly) for poly in polys]
	queue = [(heuristic(0, 0), 0, 0)]
	
	best[0][0] = 0
	added_vertices = set([(0, 0)])

	while queue:

		_, i, j = heapq.heappop(queue)
		if i == len(polys) - 1:

			path = []
			current = (i, j)

			while current is not None:
				path.append(polys[current[0]][current[1]])
				current = previous[current[0]][current[1]]

			return path[::-1]
		
		for k in range(len(polys[i + 1])):

			cost = best[i][j] + math.dist(polys[i][j], polys[i + 1][k])

			if cost < best[i + 1][k]:

				best[i + 1][k] = cost
				previous[i + 1][k] = (i, j)

				if (i + 1, k) not in added_vertices:
					heapq.heappush(queue, (cost + heuristic(i + 1, k), i + 1, k))
					added_vertices.add((i + 1, k))
	
	raise ValueError("No path found")

def project_point_polygon(point: tuple[float, float], polygon: Sequence[tuple[float, float]]) -> tuple[float, float]:

	best_projection = None
	best_distance = math.inf

	for i in range(len(polygon)):

		a = polygon[i]
		b = polygon[(i + 1) % len(polygon)]

		ap = (point[0] - a[0], point[1] - a[1])
		ab = (b[0] - a[0], b[1] - a[1])

		t = (ap[0] * ab[0] + ap[1] * ab[1]) / (ab[0] ** 2 + ab[1] ** 2)

		if t < 0:
			projection = a
		elif t > 1:
			projection = b
		else:
			projection = (a[0] + t * ab[0], a[1] + t * ab[1])

		distance = math.dist(point, projection)

		if distance < best_distance:
			best_distance = distance
			best_projection = projection

	return best_projection # type: ignore

def tpp_solve(start: tuple[float, float], target: tuple[float, float], polygons: Sequence[Sequence[tuple[float, float]]], *, simplify: bool = False) -> list[tuple[float, float]]:

	def bound(instance: Sequence[int]) -> float:

		selected_polygons = [convex_pieces[i][instance[i]] for i in range(len(instance))]
		input_polygons = selected_polygons + [convex_hulls[i] for i in range(len(instance), len(polygons))]

		try:
			path = u_tpp.tpp_solve(start, target, input_polygons, simplify=False)
		except ValueError:
			return float('inf')

		return path_length(path)

	if simplify:
		polygons = [u_tpp.clean_polygon(poly) for poly in polygons]

	convex_hulls = [Polygon2(poly).convex_hull().to_list() for poly in polygons]
	convex_pieces = [hertel_mehlhorn(poly) for poly in polygons]

	path_a_star = tpp_astar(start, target, convex_hulls, simplify=False)

	for point, pieces in zip(path_a_star, convex_pieces):
		pieces.sort(key=lambda poly: math.dist(point, project_point_polygon(point, poly)))

	minimal_path = []
	minimal_length = math.inf

	# Each element of the queue is a list containing the index of the convex piece chosen for each polygon, in order. 
	# We start the queue as a list containing just the empty list, which represents 
	# a combination where no convex pieces have been chosen yet. 
	queue = deque([[]])

	while queue:

		current = queue.pop()

		if len(current) == len(polygons):

			# We have a complete combination of convex pieces, so we can solve the TPP for this combination.

			instance = [convex_pieces[i][current[i]] for i in range(len(polygons))]

			try:
				path = u_tpp.tpp_solve(start, target, instance, simplify=False)
			except ValueError:
				print("Failed to solve TPP for input:\n", (start, target, instance), flush=True)
				continue

			length = path_length(path)

			if length < minimal_length:
				minimal_length = length
				minimal_path = path
		else:
			
			# We have a partial combination, so we need to add the next convex piece to the queue.

			next_polygon_index = len(current)

			for i in range(len(convex_pieces[next_polygon_index])):
				
				selected = current + [i]

				if bound(selected) > minimal_length:
					continue

				queue.append(selected)

	if minimal_path is None:
		raise ValueError("No path found")

	return minimal_path

